package developer.android.santos.calculoIMC.model;

public class IMC {
    private String altura;
    private String peso;

    public IMC(){}

    public String getAltura() {
        return altura;
    }

    public void setAltura(String altura) {
        this.altura = altura;
    }

    public String getPeso() {
        return peso;
    }

    public void setPeso(String peso) {
        this.peso = peso;
    }
}
